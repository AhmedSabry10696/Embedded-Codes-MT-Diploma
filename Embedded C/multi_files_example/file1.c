/**
 * @brief add two float numbers
 * 
 * @param x first number
 * @param y second number
 * @return float
 */
float add(float x,float y)
{
    return x+y;
}

/**
 * @brief subtract two float numbers
 * 
 * @param x first number
 * @param y second number
 * @return float 
 */
float sub(float x,float y)
{
    return x-y;
}

/**
 * @brief multiply two float numbers
 * 
 * @param x first number   
 * @param y second number
 * @return float 
 */
float mul(float x,float y)
{
    return x*y;
}

/**
 * @brief divide two float numbers
 * 
 * @param x first number
 * @param y second number
 * @return float 
 */
float division(float x,float y)
{
    return x/y;
}

